package com.nt.vo;

public class EmployeeVO {
private String eno;
private String ename;
private String desg;
private String basicSalery;
public String getEno() {
	return eno;
}
public void setEno(String eno) {
	this.eno = eno;
}
public String getEname() {
	return ename;
}
public void setEname(String ename) {
	this.ename = ename;
}
public String getDesg() {
	return desg;
}
public void setDesg(String desg) {
	this.desg = desg;
}
public String getBasicSalery() {
	return basicSalery;
}
public void setBasicSalery(String basicSalery) {
	this.basicSalery = basicSalery;
}

}
